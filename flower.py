import turtle

turtle.title("Flower")
flower = turtle.Turtle()

flower.shape("circle")
flower.pensize(5)
flower.speed(15)
turtle.bgcolor("#ffffe6")

# drawing flower pot
flower.penup()
flower.goto(-130, -280)
flower.begin_fill()
flower.color('#260d0d', '#4d1919')
flower.pendown()
flower.forward(250)
flower.left(80)
flower.forward(150)
flower.right(80)
flower.backward(300)
flower.right(-99)
flower.backward(149)
flower.end_fill()
flower.penup()

# drawing flower stem
flower.goto(-65, -70)
flower.pendown()
flower.begin_fill()
flower.color("#004d00")
flower.pensize(8)
flower.backward(58)
flower.forward(240)
flower.penup()
flower.goto(5, -70)
flower.pendown()
flower.right(20)
flower.backward(58)
flower.forward(230)

flower.penup()
flower.goto(-20, -70)
flower.pendown()
flower.left(0)
flower.backward(58)
flower.forward(100)


flower.begin_fill()
flower.penup()

# drawing flower petals/ leaf
flower.goto(5, -45)
flower.pendown()
flower.pensize(5)
flower.begin_fill()
flower.color("#004d00", "#006600")
flower.circle(-90, 90)
flower.goto(5, -45)
flower.circle(90, 90)
flower.penup()
flower.end_fill()

flower.goto(1, -115)
flower.pendown()
flower.pensize(5)
flower.begin_fill()
flower.color("#004d00", "#006600")
flower.circle(-90, 95)
flower.goto(2, -115)
flower.circle(90, 95)
flower.penup()
flower.end_fill()

flower.goto(-59, -125)
flower.pendown()
flower.pensize(6)
flower.begin_fill()
flower.color("#004d00", "#006600")
flower.circle(100, 100)
flower.goto(-59, -125)
flower.circle(-100, 100)
flower.penup()
flower.end_fill()

# drawing flowers
flower.pensize(4)
flower.goto(32, 85)
flower.dot(50, '#ffe680')

flower.pendown()
flower.color("#e60000")
flower.penup()
flower.goto(60, 40)
flower.pendown()
for i in range(15):
    flower.circle(100-i, 60)
    flower.left(90)

flower.penup()
flower.goto(-88, 95)
flower.pendown()
flower.dot(65, '#ffe680')
flower.penup()
flower.goto(-31, 130)
flower.pendown()
for i in range(25):
    flower.circle(130-i, 60)
    flower.left(90)

flower.penup()
flower.goto(-12, -20)
flower.pendown()
flower.dot(20, '#ffe680')
flower.penup()
flower.goto(-35, -20)
flower.pendown()
for i in range(8):
    flower.circle(40-i, 60)
    flower.left(90)

flower.hideturtle()
turtle.done()
